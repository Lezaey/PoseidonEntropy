def gen_pass(size, uppercase, lowercase, numbers, symbols):
    characters = ""

    if uppercase:
        characters += string.ascii_uppercase
    if lowercase:
        characters += string.ascii_lowercase
    if numbers:
        characters += string.digits
    if symbols:
        characters += string.punctuation

    if not characters:
        return None

    password = ''.join(secrets.choice(characters) for _ in range(size))
    return password


def calculate_entropy(password):
    pool = len(set(password))
    entropy = len(password) * math.log2(pool) if pool > 0 else 0
    return round(entropy, 2)


def evaluate_strength(entropy):
    if entropy < 40:
        return "Weak"
    elif entropy < 60:
        return "Medium"
    else:
        return "Strong"


